import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const repoRoot = path.resolve(__dirname, '../../..');
const primitivesDir = path.join(repoRoot, 'packages/ui/src/primitives');

const curated = new Set(['Button', 'Input', 'Checkbox', 'Dialog']);

function isDirectoryEntry(ent) {
  return ent && typeof ent === 'object' && ent.isDirectory?.();
}

function storyTemplate(name) {
  return `// @generated by packages/ui/scripts/refresh-auto-stories.mjs
import { ${name} } from './${name}.jsx';
import { SlotsList, AutoPreview } from '../../storybook/AutoStories.jsx';

export default {
  title: 'Primitives/${name}'
};

export const Slots = {
  render: () => <SlotsList title="${name} slots" value={${name}} />
};

export const Preview = {
  render: () => <AutoPreview name="${name}" primitive={${name}} />
};
`;
}

async function main() {
  const entries = await fs.readdir(primitivesDir, { withFileTypes: true });
  const primitiveDirs = entries
    .filter(isDirectoryEntry)
    .map((ent) => ent.name)
    .filter((name) => !name.startsWith('_'))
    .sort((a, b) => a.localeCompare(b));

  let updated = 0;
  let skipped = 0;

  for (const name of primitiveDirs) {
    if (curated.has(name)) {
      skipped += 1;
      continue;
    }
    const storyFile = path.join(primitivesDir, name, `${name}.stories.jsx`);
    await fs.writeFile(storyFile, storyTemplate(name), 'utf8');
    updated += 1;
  }

  console.log(`Story files: updated=${updated}, skipped(curated)=${skipped}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});


